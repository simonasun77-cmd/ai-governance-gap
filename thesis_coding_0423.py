def build_combined_table(models, columns, var_order, footer_rows):
    """
    Build a combined regression table.

    Parameters
    ----------
    models : list of fitted models (one per column)
    columns : list of column header names
    var_order : list of (display_name, internal_name) tuples
    footer_rows : list of (label, [values]) tuples

    Returns
    -------
    pandas DataFrame with two rows per variable (coef + SE)
    """
    rows = []
    for display, internal in var_order:
        coef_row = [display]
        se_row = ['']
        for m in models:
            c, s = extract_row(m, internal)
            coef_row.append(c)
            se_row.append(s)
        rows.append(coef_row)
        rows.append(se_row)

    # Footer
    for label, values in footer_rows:
        rows.append([label] + values)

    return pd.DataFrame(rows, columns=['Variable'] + columns)


