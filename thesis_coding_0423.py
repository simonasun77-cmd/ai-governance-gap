def weighted_crosstab(df, row, col, weight):
    categories_row = sorted(df[row].dropna().unique())
    categories_col = df[col].cat.categories

    table = pd.DataFrame(index=categories_row, columns=categories_col)

    for c in categories_col:
        sub = df[df[col] == c]
        total_w = sub[weight].sum()
        for r in categories_row:
            val = sub[sub[row] == r][weight].sum()
            table.loc[r, c] = (val / total_w * 100) if total_w > 0 else np.nan

    return table.astype(float)

