def build_regression_table(doc, title, columns, subheader, var_order,
                           models, footer_rows, total_width=14400):
    """
    Build a regression table with:
    - title row (with top + bottom border)
    - column header row
    - subheader row (DV labels, italic)
    - bottom border under headers
    - one row pair (coef + SE) per variable
    - top border above footer
    - bottom border at end
    """
    add_table_title(doc, title)

    n_cols = len(columns)
    label_width = int(total_width * 0.28)
    data_width = (total_width - label_width) // (n_cols - 1)
    col_widths = [label_width] + [data_width] * (n_cols - 1)

    # Total rows: 1 header + 1 subheader + 2 per variable + footer rows
    n_var_rows = len(var_order) * 2
    n_footer = len(footer_rows)
    total_rows = 1 + 1 + n_var_rows + n_footer

    table = doc.add_table(rows=total_rows, cols=n_cols)
    table.autofit = False

    # All cells start with no borders, no padding
    for row in table.rows:
        for cell in row.cells:
            set_cell_borders(cell)
            set_cell_margins(cell)

    set_table_column_widths(table, col_widths)

    # Row 0: Column headers (top + bottom border on this row)
    for i, col_label in enumerate(columns):
        cell = table.rows[0].cells[i]
        align = 'left' if i == 0 else 'center'
        write_cell(cell, col_label, bold=True, size=HEADER_SIZE, align=align)
        set_cell_borders(cell, top=True, bottom=True)

    # Row 1: Subheader (DV labels), italicized
    for i, sub_label in enumerate(subheader):
        cell = table.rows[1].cells[i]
        align = 'left' if i == 0 else 'center'
        write_cell(cell, sub_label, italic=True, size=SE_SIZE, align=align)

    # Variable rows (2 per variable)
    row_idx = 2
    for display_name, internal_name in var_order:
        # Coefficient row
        write_cell(table.rows[row_idx].cells[0], display_name,
                   size=COEF_SIZE, align='left')
        for i, m in enumerate(models, start=1):
            coef_str, _ = extract_row(m, internal_name)
            write_cell(table.rows[row_idx].cells[i], coef_str,
                       size=COEF_SIZE, align='center')
        # SE row
        write_cell(table.rows[row_idx + 1].cells[0], '',
                   size=COEF_SIZE, align='left')
        for i, m in enumerate(models, start=1):
            _, se_str = extract_row(m, internal_name)
            write_cell(table.rows[row_idx + 1].cells[i], se_str,
                       italic=True, size=SE_SIZE, align='center')
        row_idx += 2

    # Footer rows
    for f_idx, (label, values) in enumerate(footer_rows):
        is_first = f_idx == 0
        is_last = f_idx == len(footer_rows) - 1

        cells = table.rows[row_idx].cells
        write_cell(cells[0], label, size=COEF_SIZE, align='left')
        if is_first:
            set_cell_borders(cells[0], top=True)
        if is_last:
            set_cell_borders(cells[0], bottom=True,
                             top=is_first)

        for i, v in enumerate(values, start=1):
            write_cell(cells[i], v, size=COEF_SIZE, align='center')
            if is_first:
                set_cell_borders(cells[i], top=True)
            if is_last:
                set_cell_borders(cells[i], bottom=True,
                                 top=is_first)
        row_idx += 1

    return table


def build_simple_table(doc, title, rows, total_width=14400):
    """Build a simple table where row[0] is the header row."""
    add_table_title(doc, title)

    n_cols = len(rows[0])
    col_width = total_width // n_cols
    col_widths = [col_width] * n_cols

    table = doc.add_table(rows=len(rows), cols=n_cols)
    table.autofit = False

    for row in table.rows:
        for cell in row.cells:
            set_cell_borders(cell)
            set_cell_margins(cell)

    set_table_column_widths(table, col_widths)

    for r_idx, row_data in enumerate(rows):
        is_header = r_idx == 0
        is_last = r_idx == len(rows) - 1
        for c_idx, val in enumerate(row_data):
            cell = table.rows[r_idx].cells[c_idx]
            align = 'left' if c_idx == 0 else 'center'
            write_cell(cell, str(val), bold=is_header,
                       size=COEF_SIZE, align=align)
            if is_header:
                set_cell_borders(cell, top=True, bottom=True)
            if is_last:
                set_cell_borders(cell, bottom=True)
    return table


